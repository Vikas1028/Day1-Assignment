public class Primitive_Data_Type {
	
	static byte Number1;
	static short Number2;
    static int Number3;
    static long Number4;
    static float Number5;
    static double Number6;
    static boolean value;
    static String name;
    
    public static void main(String[] args) {
    	System.out.println("Default value of Primitive Data Type ");
    	System.out.println("Byte : " + Number1);
    	System.out.println("Short : " + Number2);
    	System.out.println("Int : " + Number3);
    	System.out.println("Long : " + Number4);
    	System.out.println("Float : " + Number5);
    	System.out.println("Double : " + Number6);
    	System.out.println("Boolean : " + value);
    	System.out.println("String : " + name);
    	}
	
}
