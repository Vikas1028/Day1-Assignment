public class Strings_are_Equal {

	public static void main(String[] args) {
		String name1 = "Vikas";
		String name2 = "Ritik";
		System.out.println("Name1: " + name1);
		System.out.println("Name2: " + name2);
		if (name1 == name2) {
			System.out.println("Name1 String and Name2 String are Equal");
		} else {
			System.out.println("Name1 String and Name2 String are NotEqual");
		}
	}
}
